/**
 * Orientation is an enumeration that contains all possible orientation of a robot
 * 
 * @author Ying Hao
 * @since 21 Feb 2018
 * @version 2.0
 */
public enum Orientation {
	/**
	 * Facing NORTH
	 */
	NORTH,
	/**
	 * Facing SOUTH
	 */
	SOUTH,
	/**
	 * Facing EAST
	 */
	EAST,
	/**
	 * Facing WEST
	 */
	WEST
}
